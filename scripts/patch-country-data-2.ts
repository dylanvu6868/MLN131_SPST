function applyPatch(existing: Record<string, unknown>, patch: Record<string, unknown>): Record<string, unknown> {
  const result = { ...existing };
  for (const [key, value] of Object.entries(patch)) {
    const current = result[key];
    const isStale =
      current === "Needs verification" ||
      current === "Data unavailable" ||
      current === null ||
      current === undefined ||
      current === "";

    const alwaysUpdate = [
      "headOfState", "headOfGovernment", "rulingParty",
      "headOfStateTitle", "headOfGovernmentTitle",
      "dataUpdatedAt", "confidenceLevel",
    ];

    if (isStale || alwaysUpdate.includes(key)) {
      result[key] = value;
    }
  }
  return result;
}

function main() {
  if (!fs.existsSync(DB_PATH)) {
    console.error(`❌ Database not found at: ${DB_PATH}`);
    process.exit(1);
  }

  const db = new DatabaseSync(DB_PATH, { timeout: 10000 });
  db.exec("PRAGMA journal_mode = WAL");

  const rows = db
    .prepare("SELECT iso3, profile_json FROM country_profiles")
    .all() as Array<{ iso3: string; profile_json: string }>;

  console.log(`📊 Loaded ${rows.length} country profiles from DB`);

  const update = db.prepare(
    "UPDATE country_profiles SET profile_json = ?, data_updated_at = ? WHERE iso3 = ?"
  );

  let patchedCount = 0;

  db.exec("BEGIN IMMEDIATE TRANSACTION");
  try {
    for (const row of rows) {
      const iso3 = row.iso3;
      const patch = PATCHES[iso3];
      if (!patch) continue;

      const profile = JSON.parse(row.profile_json) as Record<string, unknown>;
      const patched = applyPatch(profile, patch);

      update.run(
        JSON.stringify(patched),
        String(patched.dataUpdatedAt ?? "2026-06-05"),
        iso3
      );

      patchedCount++;
      console.log(`  ✅ Patched: ${iso3} (${patched.countryName ?? iso3})`);
    }

    db.exec("COMMIT");
  } catch (error) {
    db.exec("ROLLBACK");
    throw error;
  } finally {
    db.close();
  }

  console.log(`\n✅ Done! Patched ${patchedCount} countries in batch 2.`);
}

main();
